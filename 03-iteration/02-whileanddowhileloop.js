//while

let i = 0
while (i <= 10) {
    console.log(`Value of i is ${i}`);
    i = i + 2
}


let array = ["Afifa","Mama","Dad"]

let arr = 0
while (arr < array.length) {
    console.log(`Value is ${array[arr]}`);
    arr = arr + 1
}


